package org.curtis.musicxml.note;

public class NoteType {
    private String value;
    private String size;

    public NoteType() {

    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
}
