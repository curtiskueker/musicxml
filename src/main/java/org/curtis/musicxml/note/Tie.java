package org.curtis.musicxml.note;

public class Tie {
    private String type;
    private String timeOnly;

    public Tie() {

    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getTimeOnly() {
        return timeOnly;
    }

    public void setTimeOnly(String timeOnly) {
        this.timeOnly = timeOnly;
    }
}
